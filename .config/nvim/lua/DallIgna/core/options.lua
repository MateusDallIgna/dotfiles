local opt = vim.opt

-- ===========================================================================================
--  TAB & INDENTATION
-- ===========================================================================================

opt.tabstop = 4        
opt.shiftwidth = 4     
opt.expandtab = true   
opt.autoindent = true  

-- ===========================================================================================
--  SEARCH
-- ===========================================================================================

opt.ignorecase = true  
opt.smartcase = true   

-- ===========================================================================================
--  UI & APPEARANCE
-- ===========================================================================================

opt.number = true          
opt.relativenumber = true  
opt.termguicolors = true   
opt.signcolumn = "yes"     
opt.scrolloff = 8          

-- ===========================================================================================
--  SYSTEM & BEHAVIOR
-- ===========================================================================================

opt.clipboard = "unnamedplus" 
opt.splitbelow = true         
opt.splitright = true         
opt.wrap = false              

-- ===========================================================================================
--  VIRTUAL EDIT
-- ===========================================================================================

opt.virtualedit = "block"  
